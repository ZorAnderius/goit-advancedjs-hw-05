export const add = (num1: number, num2: number) => num1 + num2;

export const multiply = (num1: number, num2: number) => num1 * num2;

export const subtraction = (num1: number, num2: number) => num1 - num2;

export const division = (num1: number, num2: number) => num2 ? num1 / num2 : NaN;

export const remainder = (num1: number, num2: number) => num2 ? num1 % num2 : NaN;

export const pow = (num1: number, num2: number) => Math.pow(num1, num2);


